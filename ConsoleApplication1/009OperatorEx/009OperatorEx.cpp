#include <iostream>

int main()
{
	int PlayerHp = 0;
	int MonsterAtt = 0;

	PlayerHp = 10;
	MonsterAtt = 2;

	//PlayerHp 에서 MonsterAtt 의 값을 뺀 값을 다시 집어 넣어라
	//귀찮아서 만들어진 연산자
	//축약연산자 혹은 복합할당 연산자 라고 한다. 
	PlayerHp -= MonsterAtt;

	
}

	
	

